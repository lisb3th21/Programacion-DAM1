# Convertir los grados gentígrados en Fahrenheit
print("¡Bienvenido! Este programa le facilitara la conversión de grados centigrados a Fahrenheit.")
C = float(input("Por favor ingrese los grados centigrados que desee convertir: "))
F = C*(9/5)+32
print(C, "º equivale a ", F, "Fahrenheits. \nGracias por utilizar nuestro sevicio :)")
