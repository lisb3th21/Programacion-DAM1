#velovidad media de un coche 
print ("Bienvenido. Este programa lo ayudara a calcular la velocidad media del recorrido que ha hecho su auto.")
distancia = float (input("¿Cuántos metros ha recorrido? "))
tiempo = float (input("¿Cuántos minutos ha tardado en recorrer esta distancia? "))
Tmedio= distancia / tiempo
print ("La velocidad media de su trayecto ha sido de ", Tmedio, "m/min. Gracias por utilizar este servicio \n ¡Hasta la proxima!")