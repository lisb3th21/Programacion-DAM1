#Hacer un diagrama para calcular y mostrar el área de un triángulo :)
print ("¡Bienvenido! Este programa le ayudará a calcular el área de su triángulo. \n Por favor, introduzca valos necesarios.")
b = input("Introduzca la medida de la base del triangulo: ")
h = input("Introduzca la medida de la altura del triangulo: ")
resultado = float(b)*float(h) / 2
print ("El área del triángulo es de ", (resultado), "\nGracias por utilizar nuestro servicio.")
